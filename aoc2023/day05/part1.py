from __future__ import annotations

import argparse
import os.path

import pytest

import support

INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def compute(s: str) -> int:
    lines = s.splitlines()

    seeds = [*map(int, lines[0].split(': ')[1].split())]
    maps = [[[*map(int, n.split())] for n in i.split('\n')[1:]] for i in '\n'.join(lines[2:]).split('\n\n')]

    locations = []

    for current_seed in seeds:
        for current_map in maps:
            for dest, src, rng in current_map:
                if src <= current_seed < (src + rng):
                    current_seed = dest + (current_seed - src)
                    break
        locations.append(current_seed)

    return min(locations)


